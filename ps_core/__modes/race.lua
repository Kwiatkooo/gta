function formatRaceTime(milliseconds)
    local totalseconds = math.floor(milliseconds/1000)
    milliseconds = milliseconds % 1000
    local seconds = totalseconds % 60
    local minutes = math.floor(totalseconds/60)
    minutes = minutes % 60
    return string.format("%02d:%02d",minutes,seconds)  
end

function raceCountDownText(text)
    for i,v in pairs(getPlayersInTeam(race.team)) do
        local textDisplay = textCreateDisplay()
        local textItem = textCreateTextItem(tostring(text),0.5,0.4,"medium",255,255,255,200,3,"center","center",200)
        textDisplayAddText(textDisplay,textItem)
        textDisplayAddObserver(textDisplay,v)
        setTimer(textDestroyTextItem,800,1,textItem)
        setTimer(textDestroyDisplay,800,1,textDisplay)
		if text ~= "Start!" then playSoundFrontEnd(v,44) else playSoundFrontEnd(v,45) end
	end
end

function raceGameStart()
    local remaining,executesRemaining,totalExecutes = getTimerDetails(race.timers[2])
	local __countdown = executesRemaining-1
	if __countdown ~= 0 then return raceCountDownText(__countdown) end
	raceCountDownText("Start!")
	race.time = getTickCount()
    if countPlayersInTeam(race.team) > race.minplayers then
	    for i,v in pairs(race.vehicles) do
		    setElementFrozen(v,false)
		end
	    for i,v in pairs(getPlayersInTeam(race.team)) do
		    setElementFrozen(v,false)
		end
	else
	    outputChatBox("● INFO: Nie udało się skompletować minimalnej ilości graczy na wyścig.",root,255,0,0,true)
	    --[[raceGamePlayers(false)]] raceGameEnd(true)
	end
end

function raceGameEnd(rsp)
    for i,v in pairs(race.timers) do
	    if isTimer(v) then
		    killTimer(v)
		end
	end
	for i,v in pairs(getPlayersInTeam(race.team)) do
		triggerClientEvent(v,"onServerRaceEnd",v)
		if rsp == true then
			setPlayerTeam(v,nil)
			removePedFromVehicle(v)
			triggerClientEvent(v,"Client:ShowSpawnMenu",v)
		end
	end
	for i,v in pairs(race.vehicles) do
	    if isElement(v) then destroyElement(v) end
		race.vehicles[i] = nil
	end
	--outputChatBox("● INFO: Wyścig zakończony.",root,255,0,0,true)
    race.timers = {}
	race.time = nil
    race.vehicles = {}
	race.played = false
	race.currentPos = nil
end

