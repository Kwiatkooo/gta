function raceGamePreStart()
    race.played = true
	race.currentPos = 1
    if countPlayersInTeam(race.team) > race.minplayers then
        local sRandomMap = math.random(1,#race.maps)
		local vehicleID = race.maps[sRandomMap]["vehicle"]
	    if sRandomMap then
		    if vehicleID ~= 0 then
		        for i=1,countPlayersInTeam(race.team) do
			        local spawnpoint = split(race.maps[sRandomMap]["spawnpoints"][i],",")
				    race.vehicles[i] = createVehicle(vehicleID,spawnpoint[1],spawnpoint[2],spawnpoint[3],0,0,spawnpoint[4])
					addEventHandler("onVehicleStartExit",race.vehicles[i],function(exitingPlayer,seat,jacked,door) cancelEvent() end)
				    setElementFrozen(race.vehicles[i],true)
				    setElementDimension(race.vehicles[i],race.dimension)
				    addVehicleUpgrade(race.vehicles[i],1010)
			    end
			end
            for i,v in pairs(getPlayersInTeam(race.team)) do
			    if --[[not isPlayerActiveGUI(v) and]] not isPedDead(v) and getElementData(v,"pCommands") ~= false then
			    	triggerClientEvent(v,"onClientMiniGamesStart",v)
			    	takeAllWeapons(v)
			    	setElementData(v,"pCommands",false)
			    	triggerClientEvent(v,"cMiniGamesRaceData",v,race.maps[sRandomMap]["checkpoints"],race.dimension)
					triggerClientEvent(v,"hideGuiElements",v)
					removePedFromVehicle(v)
					removePedJetPack(v)
					setElementDimension(v,race.dimension)
					setElementInterior(v,0)
					if race.vehicles[i] then
					    warpPedIntoVehicle(v,race.vehicles[i])
					else
					    local spawnpoint = split(race.maps[sRandomMap]["spawnpoints"][i],",")
						setElementPosition(v,spawnpoint[1],spawnpoint[2],spawnpoint[3])
						setElementRotation(v,0,0,spawnpoint[4])
						setElementFrozen(v,true)
					end
					triggerClientEvent(v,"Client:SetGhostMode",v)
					--triggerClientEvent(v,"clientMsgBox",v,"● Jeśli wypadniesz z trasy, wpisz cmd /r")
				else
				    outputChatBox("● INFO: Zostałeś(aś) zdyskwalifikowany(a) z wyścigu.",v,255,0,0,true)
				    setPlayerTeam(v,nil)
				end
			end
			race.timers[2] = setTimer(raceGameStart,1000,6)
		end
	else
	    outputChatBox("● INFO: Nie udało się skompletować minimalnej ilości graczy na wyścig.",root,255,0,0,true)
		--[[raceGamePlayers(false)]] raceGameEnd(false)
	end
end

